package edu.drexel.TrainDemo.models;

public class SearchModel {

	private String from;
	private String to;
	private String date;

	public String getFrom() {
		return from;
	}

	public String getTo() {
		return to;
	}

	public void SetFrom(String from) {
		this.from = from;
	}

	public void SetTo(String to) {
		this.to = to;
	}

	public String getDate() {
		return date;
	}

	public void SetDate(String date) {
		this.date = date;
	}

}
